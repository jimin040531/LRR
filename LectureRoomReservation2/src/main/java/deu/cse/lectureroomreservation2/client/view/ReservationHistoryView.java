    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // ÏÇ¨Ïö©Ïûê ÏûÖÎ†•Í∞í Ï≤òÎ¶¨
        final String fUserId = txtUserId.getText().trim();
        final String fRoom = cmbRoomSelect.getSelectedItem().toString().equals("Ï†ÑÏ≤¥") ? "" : cmbRoomSelect.getSelectedItem().toString();
        final String inputDate = txtDate.getText().trim();
        final String fDate = inputDate.equals("yyyy / mm / dd") ? "" : inputDate;

        // ÎÇ†Ïßú ÌòïÏãù Í≤ÄÏ¶ù
        if (!fDate.isEmpty() && !fDate.contains("/")) {
            JOptionPane.showMessageDialog(this, "ÎÇ†Ïßú ÌòïÏãùÏù¥ ÏûòÎ™ªÎêòÏóàÏäµÎãàÎã§.\n ex: yyyy / mm / dd");
            return;
        }

        // Í≤ÄÏÉâ Ï°∞Í±¥Ïù¥ Î™®Îëê ÎπÑÏóàÎäîÏßÄ ÌôïÏù∏
        if (fUserId.isEmpty() && fRoom.isEmpty() && fDate.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Í≤ÄÏÉâ Ï°∞Í±¥ÏùÑ ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî.");
            return;
        }

        try {
            // ÏöîÏ≤≠ Í∞ùÏ≤¥ ÏÉùÏÑ±
            ReserveManageRequest req = new ReserveManageRequest(
                    "SEARCH", fUserId, fRoom, fDate,
                    null, null, null, null, null, null
            );

            // ÏÑúÎ≤ÑÏóê ÏöîÏ≤≠
            ReserveManageResult result = client.sendReserveManageRequest(req);

            SwingUtilities.invokeLater(() -> {
                // Î¨¥Ï°∞Í±¥ ÌÖåÏù¥Î∏î Ï¥àÍ∏∞Ìôî
                DefaultTableModel model = (DefaultTableModel) tblReservationHistory.getModel();
                model.setRowCount(0);

                if (result != null) {
                    String message = result.getMessage();
                    List<String[]> list = result.getReserveList();
                    
                    if ("ÏÇ¨Ïö©Ïûê Ï†ïÎ≥¥ ÏóÜÏùå".equals(message)) {
                        JOptionPane.showMessageDialog(ReservationHistoryView.this, "ÏÇ¨Ïö©Ïûê Ï†ïÎ≥¥Î•º Ï∞æÏùÑ Ïàò ÏóÜÏäµÎãàÎã§.");
                        return;
                    }

                    if ("ÏòàÏïΩ ÏóÜÏùå".equals(message) || list == null || list.isEmpty()) {
                        JOptionPane.showMessageDialog(ReservationHistoryView.this, "ÏòàÏïΩ ÎÇ¥Ïó≠Ïù¥ ÏóÜÏäµÎãàÎã§.");
                        return;
                    }

                    // ÏòàÏïΩ ÎÇ¥Ïó≠ ÌëúÏãú
                    for (String[] row : list) {
                        model.addRow(row);
                    }

                    // ÌÖåÏù¥Î∏î UI Í∞ïÏ†ú Í∞±Ïã†
                    tblReservationHistory.revalidate();
                    tblReservationHistory.repaint();
                } else {
                    JOptionPane.showMessageDialog(ReservationHistoryView.this, "ÏÑúÎ≤Ñ ÏùëÎãµ Ïò§Î•òÏûÖÎãàÎã§.");
                }
            });

        } catch (Exception e) {
            e.printStackTrace();
            SwingUtilities.invokeLater(() -> {
                JOptionPane.showMessageDialog(ReservationHistoryView.this, "ÏÑúÎ≤Ñ ÌÜµÏã† Ï§ë Ïò§Î•ò Î∞úÏÉù: " + e.getMessage());
            });
        }
    }//GEN-LAST:event_btnSearchActionPerformed

