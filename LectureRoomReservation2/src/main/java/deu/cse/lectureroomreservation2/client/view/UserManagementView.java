        JTable targetTable = null;
        int studentRow = tblStudents.getSelectedRow();
        int professorRow = tblProfessors.getSelectedRow();

        if (studentRow != -1) {
            targetTable = tblStudents;
        } else if (professorRow != -1) {
            targetTable = tblProfessors;
        } else {
            JOptionPane.showMessageDialog(this, "ì‚­ì œí•  ì‚¬ìš©ìë¥¼ ì„ íƒí•´ì£¼ì„¸ìš”.");
            return;
        }

        int selectedRow = targetTable.getSelectedRow();

        // ì—­í•  ì§ì ‘ ì¶”ì¶œ (P, S, A ë“± ê·¸ëŒ€ë¡œ)
        String rawRole = ((String) targetTable.getValueAt(selectedRow, 0)).trim();
        String id = ((String) targetTable.getValueAt(selectedRow, 2)).trim();

        // ê´€ë¦¬ì ì‚­ì œ ë°©ì§€
        if (rawRole.equals("A")) {
            JOptionPane.showMessageDialog(this, "ê´€ë¦¬ì ê³„ì •ì€ ì‚­ì œí•  ìˆ˜ ì—†ìŠµë‹ˆë‹¤.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                String.format("ì‚¬ìš©ì [%s]ë¥¼ ì‚­ì œí•˜ì‹œê² ìŠµë‹ˆê¹Œ?", id),
                "ì‚­ì œ í™•ì¸", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            // â‘¢ role = rawRole (í…Œì´ë¸” ê°’ ê·¸ëŒ€ë¡œ)ë¡œ ìˆ˜ì •
            UserRequest req = new UserRequest("DELETE", rawRole, null, id, null, null);
            UserResult result = client.sendUserRequest(req);

            if (result.isSuccess()) {
                refreshTable(rawRole);

                txtId.setText("");
                txtName.setText("");
                txtPw.setText("");

                // ì„ íƒëœ í–‰ ì´ˆê¸°í™”í•´ì„œ ì‚­ì œ ëŒ€ìƒ í˜¼ë™ ë°©ì§€
                tblStudents.clearSelection();
                tblProfessors.clearSelection();

                JOptionPane.showMessageDialog(this, "ì‚­ì œë˜ì—ˆìŠµë‹ˆë‹¤.");
            } else {
                JOptionPane.showMessageDialog(this, result.getMessage());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ì„œë²„ ì˜¤ë¥˜: " + e.getMessage());
        }
