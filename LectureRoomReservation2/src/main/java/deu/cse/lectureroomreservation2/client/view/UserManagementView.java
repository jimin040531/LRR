    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // ÏÑúÎ≤Ñ Ïó∞Í≤∞ ÌôïÏù∏
        if (!client.isConnected()) {
            JOptionPane.showMessageDialog(this, "ÏÑúÎ≤ÑÏóê Ïó∞Í≤∞ÎêòÏßÄ ÏïäÏïòÏäµÎãàÎã§.");
            return;
        }

        // ÌïôÏÉù, ÍµêÏàò ÌÖåÏù¥Î∏î Ï§ë Ïñ¥ÎîîÏóêÏÑú ÏÑ†ÌÉù ÎêòÏóàÎäîÏßÄ ÌôïÏù∏
        JTable targetTable = null;
        int studentRow = tblStudents.getSelectedRow();
        int professorRow = tblProfessors.getSelectedRow();

        if (studentRow != -1) {
            targetTable = tblStudents;
        } else if (professorRow != -1) {
            targetTable = tblProfessors;
        } else {
            JOptionPane.showMessageDialog(this, "ÏÇ≠Ï†úÌï† ÏÇ¨Ïö©ÏûêÎ•º ÏÑ†ÌÉùÌï¥Ï£ºÏÑ∏Ïöî.");
            return;
        }

        int selectedRow = targetTable.getSelectedRow();

        // Ïó≠Ìï† ÏßÅÏ†ë Ï∂îÏ∂ú (P, S, A Îì± Í∑∏ÎåÄÎ°ú)
        String rawRole = ((String) targetTable.getValueAt(selectedRow, 0)).trim();
        String id = ((String) targetTable.getValueAt(selectedRow, 2)).trim();

        // Í¥ÄÎ¶¨Ïûê ÏÇ≠Ï†ú Î∞©ÏßÄ
        if (rawRole.equals("A")) {
            JOptionPane.showMessageDialog(this, "Í¥ÄÎ¶¨Ïûê Í≥ÑÏ†ïÏùÄ ÏÇ≠Ï†úÌï† Ïàò ÏóÜÏäµÎãàÎã§.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this,
                String.format("ÏÇ¨Ïö©Ïûê [%s]Î•º ÏÇ≠Ï†úÌïòÏãúÍ≤†ÏäµÎãàÍπå?", id),
                "ÏÇ≠Ï†ú ÌôïÏù∏", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try {
            // ‚ë¢ role = rawRole (ÌÖåÏù¥Î∏î Í∞í Í∑∏ÎåÄÎ°ú)Î°ú ÏàòÏ†ï
            UserRequest req = new UserRequest("DELETE", rawRole, null, id, null, null);
            UserResult result = client.sendUserRequest(req);

            if (result.isSuccess()) {
                refreshTable(rawRole);

                DefaultTableModel profModel = (DefaultTableModel) tblProfessors.getModel();
                DefaultTableModel studModel = (DefaultTableModel) tblStudents.getModel();

                profModel.setRowCount(0);
                studModel.setRowCount(0);

                JOptionPane.showMessageDialog(this, "ÏÇ≠Ï†úÎêòÏóàÏäµÎãàÎã§.");
            } else {
                JOptionPane.showMessageDialog(this, result.getMessage());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ÏÑúÎ≤Ñ Ïò§Î•ò: " + e.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

